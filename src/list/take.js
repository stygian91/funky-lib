import slice from "./slice";

/**
 * Takes the first `n` elements of a list,
 * where `n` is the number of elements to take.
 * The original list is left unchanged.
 *
 * @function
 * @name take
 * @param {number} n
 * @param {any[]} list
 * @returns {any[]}
 */
const take = slice(0);

export default take;
